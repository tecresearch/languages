        private void setDataTable(){
            
            DefaultTableModel  dtm=(DefaultTableModel)jTable1.getModel();
            int rc=dtm.getRowCount();
            while(rc--!=0){
                dtm.removeRow(0);
            }
            try{
                FileInputStream f=new FileInputStream("task.txt");
                Scanner sc=new Scanner(f);
                    while(true){
                        try{
                            
                            Vector row=new Vector();
                            row.add(sc.nextLine());
                            dtm.addRow(row);
                            
                        }catch(Exception ee){
                   
                            break;
                    
                }
                    }
                f.close();
                sc.close();
            }catch(Exception e){
                
            }
        }
