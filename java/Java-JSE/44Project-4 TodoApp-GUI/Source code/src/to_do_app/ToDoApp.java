    private void deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteActionPerformed
        // TODO add your handling code here:
        int ri=jTable1.getSelectedRow();
        
        if(ri!=-1){
            String task = (String)jTable1.getValueAt(ri, 0);
            JOptionPane.showConfirmDialog(null, "Are you sure ?\nTo delete the selected task : "+task);
            try{
                FileOutputStream f=new FileOutputStream("temp.txt");
                PrintStream p=new PrintStream(f);
                FileInputStream ff=new FileInputStream("task.txt");
                Scanner sc=new Scanner(ff);
                while(true){
                    try{
                      String s=sc.nextLine();
                      if(!task.equalsIgnoreCase(s)){
                          p.println(s);
                      }
                       
                    }catch(Exception es){
                        break;
                     }
                }
                p.close();
                f.close();
                ff.close();
                File file=new File("task.txt");
                file.delete();
                File tempFile=new File("temp.txt");
                File newFile=new File("task.txt");
                tempFile.renameTo(newFile);
                setDataTable();
            }catch(Exception es){
                JOptionPane.showMessageDialog(null,es.getMessage());
            }
            
            
        }else{
            
        JOptionPane.showMessageDialog(null, "No task selected");
        }
        
    }//GEN-LAST:event_deleteActionPerformed

