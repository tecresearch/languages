
public class ShorthandOperators {

	public static void main(String[] args) {
		 
		int a=12;
		a+=3;
		System.out.println(a);
		a-=3;
		System.out.println(a);
		a*=3;
		System.out.println(a);
		a/=3;
		System.out.println(a);
		a%=3;
		System.out.println(a);
		
		a++;
		System.out.println(a);
		a--;
		System.out.println(a);
		
		
	}

}
