public class SwitchEx5 {

	public static void main(String[] args) {
		int a=97;
		switch(a) {
			case 1:
				System.out.println("Hello");
				break;
			case 'a':
				System.out.println("Hi");
				break;
			default:
				System.out.println("Invalid Number");
		}
		System.out.println("Thanks for using APP");
	}

}
