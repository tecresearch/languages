
public class RelationalOperators {

	public static void main(String[] args) {
		
		System.out.println("Relational Operator is also known as comparision operator");
		int a=10;
		int b=5;
		System.out.println(a<b);
		System.out.println(a>b);
		System.out.println(a==b);
		System.out.println(a<=b);
		System.out.println(a>=b);
		System.out.println(a!=b);
	}

}

